<h1>Circuito Cerrado</h1>    
<p>El Circuito Cerrado de Televisión o CCTV es una tecnología de videovigilancia diseñada para supervisar una diversidad de ambientes y actividades. Se le denomina circuito cerrado ya que, al contrario de lo que pasa con la difusión, todos sus componentes están enlazados.</p>
